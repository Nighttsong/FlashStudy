package com.flashCard.model;

public class FlashCard {

	//model for each flashcard
	String front;
	String back;
	public String getFront() {
		return front;
	}
	public void setFront(String front) {
		this.front = front;
	}
	public String getBack() {
		return back;
	}
	public void setBack(String back) {
		this.back = back;
	}
}
